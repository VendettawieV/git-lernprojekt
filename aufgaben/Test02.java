ArrayList<AreaEffect> GetAreaData(AreaEffect[] areas, PVector pos){
    ArrayList<AreaEffect> results = new ArrayList<AreaEffect>();
    for (int i = 0; i < areas.length; i++){
        if (pos.x > areas[i].position.x - areas[i].size.x / 2 &&
                pos.x < areas[i].position.x + areas[i].size.x / 2 &&
                pos.y > areas[i].position.y - areas[i].size.y / 2 &&
                pos.y < areas[i].position.y + areas[i].size.y / 2){
            results.add(areas[i]);
        }
    }
    return results;
}
